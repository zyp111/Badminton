package com.badminton.pojo;

public class Brand {
    private int brand_id;
    private String brand_name;
    private String brand_introduce;
    private String brand_img;

    public int getBrand_id() {
        return brand_id;
    }

    public void setBrand_id(int brand_id) {
        this.brand_id = brand_id;
    }

    public String getBrand_name() {
        return brand_name;
    }

    public void setBrand_name(String brand_name) {
        this.brand_name = brand_name;
    }

    public String getBrand_introduce() {
        return brand_introduce;
    }

    public void setBrand_introduce(String brand_introduce) {
        this.brand_introduce = brand_introduce;
    }

    public String getBrand_img() {
        return brand_img;
    }

    public void setBrand_img(String brand_img) {
        this.brand_img = brand_img;
    }
}
